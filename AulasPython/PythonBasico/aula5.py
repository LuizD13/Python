"""
Operadores aritméticos:

+ :Soma
- :Subtração
* :Multiplicação
/ :Divisão
// :Divisão inteira (arredondamento do resultado)
** :Exponenciação / potenciação
% :Resto da divisão
() :Parênteses (Para alterar a ordem do cálculo)

"""

print('Adição: ',10+10) #Adição
print('Subtração: ',10-5) #Subtração
print('Multiplicação: ',10*10) #Multiplicação
print('Divisão: ',10/2) #Divisão
print('Divisão inteira / arredondada: ',10//2) #Divisão inteira
print('Potenciação (3²):',3**2) #Potenciação (3²)
print('Resto de 3//2:',3%2) #Resto da divisão

print(10*' Luiz') #Multiplicação com String = Repete valores definidos

print((5+2)*10)#Ele vai executar sempre o que está em parênteses antes

